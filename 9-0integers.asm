
 
    
 ORG 100h                     ; Start at memory address 100h

    mov dl, '9'              ; Start with ASCII for '9'

reverse_number_loop:
    mov ah, 02h              ; DOS interrupt function 02h to print character in DL
    int 21h                  ; Print the character in DL
    dec dl                   ; Decrement DL to previous character (9 -> 8 -> ... -> 0)
    cmp dl, '0' - 1          ; Check if DL is before '0'
    jge reverse_number_loop  ; Repeat if DL is >= '0'

    mov ah, 4Ch              ; DOS interrupt function 4Ch to terminate program
    int 21h                  ; Exit
end

ORG 100h                     ; Start at memory address 100h

    mov dl, 'Z'              ; Start with ASCII for 'Z'

reverse_print_loop:
    mov ah, 02h              ; DOS interrupt function 02h to print character in DL
    int 21h                  ; Print the character in DL
    dec dl                   ; Decrement DL to previous character (Z -> Y -> ... -> A)
    cmp dl, 'A' - 1          ; Check if DL is before 'A'
    jge reverse_print_loop   ; Repeat if DL is >= 'A'

    mov ah, 4Ch              ; DOS interrupt function 4Ch to terminate program
    int 21h                  ; Exit
end
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
